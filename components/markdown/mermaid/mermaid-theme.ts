type ThemeVariables = NonNullable<MermaidConfig["themeVariables"]>;

export function getMermaidThemeVariables(
  resolvedTheme: string | undefined,
): ThemeVariables {
  return resolvedTheme === "dark" ? darkThemeVariables : lightThemeVariables;
}

